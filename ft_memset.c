#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*s2;
	char	g;

	i = 0;
	s2 = (char *)s;
	g = (char)c;
	while (i < n)
	{
		s2[i] = g;
		i++;
	}
	return (s);
}
